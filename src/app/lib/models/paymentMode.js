import mongoose from "mongoose";

const paymentModel = new mongoose.Schema({
  razorpay_order_id: {
    type: String,
    required: true,
  },
  razorpay_payment_id: {
    type: String,
    required: true,
  },
  razorpay_signature: {
    type: String,
    required: true,
  },
});

export const paymentSchema =
  mongoose.models.payment || mongoose.model("payment", paymentModel);
